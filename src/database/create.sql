ADD CONSTRAINT FK_EnderecoConsultorio FOREIGN KEY (endereco_id) REFERENCES endereco(id);

ALTER TABLE endereco
ADD CONSTRAINT FK_ConsultorioEndereco FOREIGN KEY (consultorio_id) REFERENCES consultorio(id);

ALTER TABLE horario
